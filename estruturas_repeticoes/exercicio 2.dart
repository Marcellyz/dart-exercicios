void main() {
  do {
    // Contagem regressiva de 10 a 1 com o uso de laço de repetição
    print('Contagem regressiva de 10 a 1:');
    int i = 10;
    while (i >= 1) {
      print(i);
      i--;
    }
  } while (false);
}
